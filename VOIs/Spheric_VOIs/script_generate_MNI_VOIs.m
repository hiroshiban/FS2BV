% This is a simple script to generate VOIs of higher visual areas in MNI coordinates
%
% Created    : "2011-11-25 10:11:43 banh"
% Last Update: "2017-09-05 14:54:54 ban"

% The TAL coordinates used here are from papers below
% LO    :
% pFs   :
% CoS   :
% V3A   : Orban et al. (2003) Neuropsychologia
% KO    : Orban et al. (2003) Neuropsychologia
% hMT   : Orban et al. (2003) Neuropsychologia
% VIPS  : Orban et al. (2003) Neuropsychologia
% POIPS : Orban et al. (2003) Neuropsychologia
% DIPSM : Orban et al. (1999) Exp Brain Res
% DIPSA : Orban et al. (2003) Neuropsychologia
% IPL   : Pernet et al. (2004) NeuroImage
% SPL   : Sterzer et al. (2002) Neuroimage
% BA44  : Aizenstein et al. (2000) J Cog Neurosci
% BA45  : Binkofski et al. (1999) Eur J Neurosci
% IFG   : Heekeren et al. (2006) PNAS
% STS   : Orban et al. (1999) Exp Brain Res
% FEF   : Orban et al. (2003) Neuropsychologia
% DLPFC : Zoe's table
%
% Those coordinates are converted to MNI space using tal2mni function.

voi_names={'LO_lh','LO_rh',...
           'pFs_lh','pFs_rh',...
           'CoS_lh','CoS_rh',...
           'V3A_lh','V3A_rh',...
           'KO_lh','KO_rh',...
           'hMT_lh','hMT_rh',...
           'VIPS_lh','VIPS_rh',...
           'POIPS_lh','POIPS_rh',...
           'DIPSM_lh','DIPSM_rh',...
           'DIPSA_lh','DIPSA_rh',...
           'IPL_lh','IPL_rh',...
           'SPL_lh','SPL_rh',...
           'BA44_lh','BA44_rh',...
           'BA45_lh','BA45_rh',...
           'IFG_lh','IFG_rh',...
           'STS_lh','STS_rh',...
           'FEF_lh','FEF_rh',...
           'DLPFC_lh','DLPFC_rh'};
center_tal_XYZ=[-44,-67, -3; 42,-67, -4;...
                -41,-46,-20; 46,-48,-22;...
                -31,-46,-19; 29,-43,-15;...
                -24,-86,  6; 24,-86,  6;...
                -36,-82,  6; 36,-82,  6;...
                -51,-72,  0; 51,-69,  3;...
                -27,-72, 30; 30,-78, 27;...
                -18,-72, 54; 24,-75, 45;...
                -18,-60, 62; 18,-60, 62;...
                -36,-48, 60; 39,-36, 54;...
                -32,-46, 45; 63,-36, 22;...
                -22,-62, 64; 12,-66, 56;...
                -43,  9, 22; 52, 12,  1;...
                -50, 46,  4; 50, 32,  4;...
                -26, 28, -3; 46, 20,  4;...
                -57,-45, 10; 57,-45, 10;...
                -33,  0, 63; 39,  0, 60;...
                -38, 28, 22; 41, 29, 16;];
center_mni_XYZ=tal2mni(center_tal_XYZ);

radius_mm=[3,5,8,10];
for mm=1:1:numel(radius_mm)
  GenerateSphericalVOIfromMNIcoords(center_mni_XYZ,radius_mm(mm),voi_names,'MNI','',sprintf('MNI_VOIs_%dmm.voi',radius_mm(mm)));
end

CompactVOIs('.');
